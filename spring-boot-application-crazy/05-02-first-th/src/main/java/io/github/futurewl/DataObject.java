package io.github.futurewl;

public class DataObject {

    private String name;

    public DataObject(String name) {
        super();
        this.name = name;
    }

    public DataObject() {
        super();
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
